package com.github.ilyes4j.gwt.mdl.extensions.flips;

/**
 * Defines the set of possible faces that can be applied on a material Flip.
 * 
 * @author Mohamed Ilyes DIMASSI
 *
 */
public enum FlipStyle {

  /**
   * Flat looking face.
   */
  FLAT,

  /**
   * Raised looking face.
   */
  RAISED,

  /**
   * Fab looking face.
   */
  FAB,

  /**
   * Mini fab looking face.
   */
  MINIFAB,

  /**
   * Icon looking face.
   */
  ICON
}
